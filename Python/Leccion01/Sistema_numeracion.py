#Profundizando en los sistemas de númeración

#Sistema decimal
a = 10
print(f'a = {a}')

#sistema Binario
a = 0b1010
print(f'a = {a}')

#Sistema octal
a = 0o12
print(f'a = {a}')

#Sistema hexadecimal
a = 0xA
print(f'a = {a}')

#Base decimal
a = int('10', 10)
print(f'a decimal = {a}')

#Base binario
a = int('1010', 2)
print(f'a binario = {a}')

#Base octal
a = int('12', 8)
print(f'a octal = {a}')

#Base hexadecimal
a = int('A', 16)
print(f'a hexadecimal = {a}')

#Base 5 sus valores son del 0 al 4
a = int('34', 5)
print(f'a 5 = {a}')